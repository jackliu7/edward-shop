package com.edward.shop.controller;

import com.alibaba.dubbo.config.annotation.Reference;
import com.edward.pojo.TbOrder;
import com.edward.pojo.group.OrderVo;
import com.edward.pojo.group.ShopOrder;
import com.edward.service.OrderService;
import entity.PageResult;
import entity.Result;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.util.List;

/**
 * controller
 * @author Administrator
 *
 */
@RestController
@RequestMapping("/order")
public class OrderController {

	@Reference
	private OrderService orderService;
	
	/**
	 * 返回全部列表
	 * @return
	 */
	@RequestMapping("/findAll")
	public List<ShopOrder> findShopOrder(String status){
		//获取当前登录人账号
		String sellerId = SecurityContextHolder.getContext().getAuthentication().getName();
		return orderService.findShopOrder(sellerId,status);
	}
	
	
	/**
	 * 返回全部列表
	 * @return
	 */
	@RequestMapping("/findPage")
	public PageResult  findPage(int page,int rows){			
		return orderService.findPage(page, rows);
	}
	
	/**
	 * 增加
	 * @param order
	 * @return
	 */
	@RequestMapping("/add")
	public Result add(@RequestBody TbOrder order){
		
		//获取当前登录人账号
		String username = SecurityContextHolder.getContext().getAuthentication().getName();
		order.setUserId(username);
		order.setSourceType("2");//订单来源  PC
		
		try {
			orderService.add(order);
			return new Result(true, "增加成功");
		} catch (Exception e) {
			e.printStackTrace();
			return new Result(false, "增加失败");
		}
	}
	
	/**
	 * 修改
	 * @param order
	 * @return
	 */
	@RequestMapping("/update")
	public Result update(@RequestBody TbOrder order){
		try {
			orderService.update(order);
			return new Result(true, "修改成功");
		} catch (Exception e) {
			e.printStackTrace();
			return new Result(false, "修改失败");
		}
	}	
	
	/**
	 * 获取实体
	 * @param id
	 * @return
	 */
	@RequestMapping("/findOne")
	public TbOrder findOne(Long id){
		return orderService.findOne(id);		
	}

	/**
	 * 删除订单明细
	 * @param orderId
	 * @param orderItemId
	 * @return
	 */
	@RequestMapping("/delOrderItem")
	public Result delOrderItem(Long orderId,Long orderItemId){
		try {
			orderService.delOrderItem(orderId,orderItemId);
			return new Result(true, "删除成功");
		} catch (Exception e) {
			e.printStackTrace();
			return new Result(false, "删除失败");
		}
	}
	
	/**
	 * 批量删除
	 * @param ids
	 * @return
	 */
	@RequestMapping("/delete")
	public Result delete(Long [] ids){
		try {
			orderService.delete(ids);
			return new Result(true, "删除成功"); 
		} catch (Exception e) {
			e.printStackTrace();
			return new Result(false, "删除失败");
		}
	}
	
		/**
	 * 查询
	 * @param order
	 * @return
	 */
	@RequestMapping("/search")
	public List<ShopOrder> search(@RequestBody TbOrder order){
		//获取当前登录人账号
		String sellerId = SecurityContextHolder.getContext().getAuthentication().getName();

		return orderService.search(sellerId,order);
	}
	
}
